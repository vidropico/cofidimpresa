package it.cofidimpresa.datamodel;

public class BancheModel {

	private int idBanche;
	private String nomeBanche;
	private String indirizzoBanche;
	private String filialeBanche;

	public int getIdBanche() {
		return idBanche;
	}

	public void setIdBanche(int idBanche) {
		this.idBanche = idBanche;
	}

	public String getNomeBanche() {
		return nomeBanche;
	}

	public void setNomeBanche(String nomeBanche) {
		this.nomeBanche = nomeBanche;
	}

	public String getIndirizzoBanche() {
		return indirizzoBanche;
	}

	public void setIndirizzoBanche(String indirizzoBanche) {
		this.indirizzoBanche = indirizzoBanche;
	}

	public String getFilialeBanche() {
		return filialeBanche;
	}

	public void setFilialeBanche(String filialeBanche) {
		this.filialeBanche = filialeBanche;
	}

}
