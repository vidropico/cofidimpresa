package it.cofidimpresa.datamodel;

public class AtecoModel {
	
	private int idAteco;
	private String codiceAteco;
	private String descrizione;
	
	public int getIdAteco() {
		return idAteco;
	}
	public void setIdAteco(int idAteco) {
		this.idAteco = idAteco;
	}
	public String getCodiceAteco() {
		return codiceAteco;
	}
	public void setCodiceAteco(String codiceAteco) {
		this.codiceAteco = codiceAteco;
	}
	public String getDescrizione() {
		return descrizione;
	}
	public void setDescrizione(String descrizione) {
		this.descrizione = descrizione;
	}

}
